class Solution:
    def maxProfit(self, prices: List[int]) -> int:
        """
        DP
        当前之前卖出 / 当天卖出
        """
        n = len(prices)
        if n == 0 or n == 1:
            return 0

        dp = [0] * (n + 1)
        minPrice = prices[0]
        for i in range(2, n + 1):
            minPrice = min(minPrice, prices[i - 1])
            dp[i] = max(dp[i - 1], prices[i - 1] - minPrice)
        
        return dp[n]
    # endFunc
# endClass

