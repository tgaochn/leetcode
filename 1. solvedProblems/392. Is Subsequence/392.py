class Solution:
    def isSubsequence(self, s: str, t: str) -> bool:
        if not s:
            return True
        i = 0
        j = 0
        n = len(t)
        for i in range(n):
            if t[i] == s[j]:
                j += 1
                if j == len(s):
                    return True
        return False
