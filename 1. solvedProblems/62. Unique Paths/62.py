class Solution:
    def uniquePaths(self, m: int, n: int) -> int:
        """
        DP using 2D array
        """

        if m == 0 or n == 0:
            return 0

        if m <= 1 or n <= 1:
            return 1
        
        dp = [[0] * (n) for _ in range(m)]
        for i in range(m):
            for j in range(n):
                if i == 0 or j == 0:
                    dp[i][j] = 1

        for i in range(1, m):
            for j in range(1, n):
                dp[i][j] = dp[i-1][j] + dp[i][j-1]

        return dp[m-1][n-1]
    # endFunc
# endClass

