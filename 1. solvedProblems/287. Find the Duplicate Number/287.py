class Solution:
    def findDuplicate(self, nums: List[int]) -> int:
        """
        利用元素在[1, n]的特殊性使用抽屉原理
        """
        l, r = min(nums), max(nums)

        def cntLess(val):
            cnt = 0
            for ele in nums:
                if ele <= val:
                    cnt += 1
            return cnt

        while l < r:
            mid = (l + r) // 2
            if cntLess(mid) > mid:
                r = mid
            else:
                l = mid + 1

        return l
    # endFunc
# endClass

