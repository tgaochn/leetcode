class Solution:
    def majorityElement(self, nums: List[int]) -> int:
        """
        hash
        """
        threshold = len(nums) // 2
        myHash = {}
        for ele in nums:
            myHash.setdefault(ele, 0)
            myHash[ele] += 1

        for ele, freq in myHash.items():
            if freq > threshold:
                return ele
    # endFunc
# endClass

