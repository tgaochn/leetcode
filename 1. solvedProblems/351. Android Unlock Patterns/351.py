class Solution:
    def numberOfPatterns(self, m: int, n: int) -> int:
        limit = {
            1: {3:2, 7:4, 9:5},
            2: {8:5},
            3: {1:2, 7:5, 9:6},
            4: {6:5},
            5: {},
            6: {4:5},
            7: {1:4, 3:5, 9:8},
            8: {2:5},
            9: {1:5, 3:6, 7:8},
        }
        self.solCnt = 0

        def bt(sol):
            k = len(sol)
            if k > n: return
            if m <= k <= n:
                # print(sol)
                self.solCnt += 1

            curEle = sol[-1]
            curNeibLis = [ele for ele in range(1, 10) if ele not in limit[curEle] or limit[curEle][ele] in sol]
            for neib in curNeibLis:
                if neib in sol: continue
                bt(sol + [neib])

        for i in range(1, 10):
            bt([i])

        return self.solCnt
    # endFunc
# endClass

