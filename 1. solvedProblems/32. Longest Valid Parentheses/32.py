class Solution:
    def longestValidParentheses(self, s: str) -> int:
        """
        回文, 括号匹配等一律用stack来做
        """
        if not s: return 0
        myStack = []
        n = len(s)
        rlt = 0

        for i in range(n):
            curChar = s[i]
            # start of a new group or (
            if (not myStack or s[myStack[-1]] == ')') or curChar == '(':
                myStack.append(i)
            else:
                myStack.pop()
                rlt = max(rlt, i - (myStack[-1] if myStack else -1))
        
        return rlt
    # endFunc
