class Solution:
    def characterReplacement(self, s: str, k: int) -> int:
        """
        sliding window
        """
        if not s: return 0

        l = 0
        r = 0
        n = len(s)
        maxCharCnt = 0
        maxLen = 0
        charCntMap = {}
        while r < n:
            charCntMap.setdefault(s[r], 0)
            charCntMap[s[r]] += 1
            maxCharCnt = max(maxCharCnt, charCntMap[s[r]])
            if r - l + 1 <= maxCharCnt + k:
                maxLen = max(maxLen, r - l + 1)
            else:
                charCntMap[s[l]] -= 1
                l += 1
            r += 1
        return maxLen
    # endFunc
# endClass

