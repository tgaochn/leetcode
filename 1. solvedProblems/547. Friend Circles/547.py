class Solution:
    def findCircleNum(self, M: List[List[int]]) -> int:
        prevMap = {}
        cnt = len(M)

        def find(cur):
            prevMap.setdefault(cur, cur)
            if prevMap[cur] != cur:
                updatedPrev = find(prevMap[cur])
                prevMap[cur] = updatedPrev
            return prevMap[cur]
        
        def union(x, y):
            root1 = find(x)
            root2 = find(y)
            prevMap[root1] = root2
        
        def isFriend(i, j):
            if M[i][j] == 1:
                return True
            friendSet1 = set([idx for idx, flag in enumerate(M[i]) if flag])
            friendSet2 = set([idx for idx, flag in enumerate(M[j]) if flag])

            if friendSet1.intersection(friendSet2):
                return True
            
            return False

        for i in range(cnt):
            for j in range(cnt):
                if i >= j: continue
                if isFriend(i, j):
                    union(i, j)
        
        cycleCnt = 0
        for i in range(cnt):
            if find(i) == i:
                cycleCnt += 1
        
        return cycleCnt
    # endFunc
# endClass

