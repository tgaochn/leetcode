ListNode = singleLinkedList.ListNode
TreeNode = binaryTree.TreeNode
Node = nTree.Node
null = None

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def preorderTraversal(self, root: TreeNode) -> List[int]:
        rlt = []

        def traversal(root):
            if root is not None:
                rlt.append(root.val)
                traversal(root.left)
                traversal(root.right)
        
        traversal(root)
        return rlt
    # endFunc
# endClass

def func():
    s = Solution()

    # !! change function name and para here
    myFuncLis = [
        s.preorderTraversal,
    ]
