class Solution:
    def rob(self, nums: List[int]) -> int:
        """
        DP with space cut
        """
        n = len(nums)

        if n == 0:
            return 0

        prev = 0
        cur = nums[0]
        for i in range(2, n+1):
            prev, cur = cur, max(cur, prev + nums[i - 1])
        
        return cur
    # endFunc

    def rob2(self, nums: List[int]) -> int:
        """
        DP
        """

        if len(nums) == 0:
            return 0

        n = len(nums)
        dp = [0] * (n + 1)
        dp[0] = 0
        dp[1] = nums[0]
        for i in range(2, n + 1):
            dp[i] = max(dp[i - 2] + nums[i - 1], dp[i - 1])

        return dp[n]

    def rob1(self, nums: List[int]) -> int:
        """
        backtracking, exceed the time limit
        """
        rlt = []
        n = len(nums)

        def bt(sol, i):
            # pruning
            m = len(sol)
            for j in range(m):
                if sol[j] == False and j + 1 < m and sol[j + 1] == False and j + 2 < m and sol[j + 2] == False:
                    return

            # end
            if i == n:
                rlt.append(sum([nums[j] for j, picked in enumerate(sol) if picked]))
                return

            # update
            candSolLis = [False] if len(sol) > 0 and sol[-1] else [True, False]
            for candSol in candSolLis:
                bt(sol + [candSol], i + 1)

        bt([], 0)
        return max(rlt)
    # endFunc
# endClass

