class Solution:
    def twoSum(self, nums, target):
        h = {}
        n = len(nums)
        for i in range(n):
            if target - nums[i] in h:
                return h[target - nums[i]], i
            else:
                h[nums[i]] = i
        pass
    # endFunc
# endClass

