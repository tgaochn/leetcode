class Solution:
    def addTwoNumbers(self, l1: ListNode, l2: ListNode) -> ListNode:
        if not l1 and not l2: return None
        if not l1: return l2
        if not l2: return l1

        eleSum = l1.val + l2.val
        flag = 1 if eleSum >= 10 else 0
        ele = eleSum % 10
        rltListNode = ListNode(ele)
        curNode = rltListNode
        l1 = l1.next
        l2 = l2.next

        while l1 or l2:
            ele1 = 0 if not l1 else l1.val
            ele2 = 0 if not l2 else l2.val
            if l1:
                l1 = l1.next
            if l2:
                l2 = l2.next

            eleSum = ele1 + ele2 + flag
            ele = eleSum % 10
            flag = 1 if eleSum >= 10 else 0
            curNode.next = ListNode(ele)
            curNode = curNode.next
        
        if flag == 1:
            curNode.next = ListNode(1)

        return rltListNode
    # endFunc
# endClass

