class Solution:
    def findContentChildren(self, g: List[int], s: List[int]) -> int:
        j = 0
        g.sort()
        s.sort()
        cnt = 0
        n = len(g)
        for _, cookie in enumerate(s):
            if j >= n:
                return cnt
            require = g[j]
            if cookie >= require:
                cnt += 1
                j += 1
        return cnt
    # endFunc
# endClass

