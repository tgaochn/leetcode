class Solution:
    def combinationSum1(self, candidates: List[int], target: int) -> List[List[int]]:
        """
        backtracking
        """

        rlt = []
        n = len(candidates)

        def bt(sol, curTar, i):
            # step 3: pruning
            if curTar < 0:
                return

            # step 1: end condition
            if i == n:
                if curTar == 0:
                    rlt.append(sol)
                return 
            
            # step 2: update answer and next step
            #   case1: pick
            #   case2: not pick and move to next
            bt(sol + [candidates[i]], curTar - candidates[i], i)
            bt(sol, curTar, i + 1)

        bt([], target, 0)
        return rlt
    # endFunc
# endClass

